print(10 + 5) # penjumlahan
print(10 - 5) # pengurangan
print(10 * 5) # perkalian
print(10 / 5) # pembagian
print(15 // 5) # floor division pembagian pembulatan adalah pembulatan ke bawah pada pembagian bulat 
print(11 % 5) # modulus adalah sisa pembagian
print(10 ** 2) # eksponen adalah pangkat


