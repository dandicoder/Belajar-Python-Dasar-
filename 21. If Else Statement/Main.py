# If dan Else Statement

# 1. if
# 2. kondisi
# 3. aksi

# 1. program if inline
nama = input("Siapa nama kamu? ")
if nama == "Dandi": print(f"Halo {nama}")

print("Program selesai")

# 2. program if multiline
nama = input("Siapa nama kamu? ")   
if nama == "Dandi":
    print(f"Halo {nama}")
    print("Program selesai")

# 3. program if multiline dengan else
nama = input("Siapa nama kamu? ")   
if nama == "Dandi":
    print(f"Halo {nama}")
else:
    print("Program selesai")