
# inputan pasti string
data_string = input("masukkan data_string : ")
print("data_string : ", data_string, " bertipe : ", type(data_string))

# inputan pasti integer
data_int = int(input("masukkan data_int integer : "))
print("data_int : ", data_int, " bertipe : ", type(data_int))

# inputan pasti float
data_float = float(input("masukkan data_float float : "))
print("data_float : ", data_float, " bertipe : ", type(data_float))

# inputan pasti boolean
data_bolean = bool(int(input("masukkan data_bolean boolean: ")))
print("data_bolean : ", data_bolean, " bertipe : ", type(data_bolean))

